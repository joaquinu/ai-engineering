"""
Comparison and Reporting Layer

Contains statistical comparison logic between evaluation runs.
"""

import statistics
from .stats import bootstrap_confidence_interval, wilson_confidence_interval

def compare_eval_runs(baseline_results, new_results, criteria=None):
    """
    Compares two evaluation runs and flags significant regressions or improvements.
    
    Uses bootstrap and Wilson confidence intervals to determine statistical bands
    and compares mean score deltas.
    
    Args:
        baseline_results (list[EvalResult]): Baseline run results.
        new_results (list[EvalResult]): New candidate run results.
        criteria (list[str], optional): Criteria list. Defaults to None.
        
    Returns:
        dict: A structured report containing comparison statistics, ship decisions,
              and category/criteria-wise status.
    """
    if criteria is None:
        criteria = ["relevance", "correctness", "helpfulness", "safety"]

    report = {"criteria": {}, "overall": {}, "regressions": [], "improvements": []}

    for criterion in criteria:
        # 1. Extract scores matching the current criterion
        baseline_scores = _extract_scores_by_criterion(baseline_results, criterion)
        new_scores = _extract_scores_by_criterion(new_results, criterion)

        if not baseline_scores or not new_scores:
            continue

        # 2. Compute mean scores and differences
        baseline_mean = statistics.mean(baseline_scores)
        new_mean = statistics.mean(new_scores)
        diff = new_mean - baseline_mean

        # 3. Compute 95% confidence intervals for means (tells us precision of the mean)
        baseline_ci = bootstrap_confidence_interval(baseline_scores)
        new_ci = bootstrap_confidence_interval(new_scores)

        # 4. Compute pass rates (scores >= 4) with Wilson confidence intervals
        passing_baseline = sum(1 for s in baseline_scores if s >= 4)
        passing_new = sum(1 for s in new_scores if s >= 4)
        baseline_pass_rate = wilson_confidence_interval(passing_baseline, len(baseline_scores))
        new_pass_rate = wilson_confidence_interval(passing_new, len(new_scores))

        criterion_report = {
            "baseline_mean": round(baseline_mean, 3),
            "new_mean": round(new_mean, 3),
            "diff": round(diff, 3),
            "baseline_ci": baseline_ci,
            "new_ci": new_ci,
            "baseline_pass_rate": f"{passing_baseline}/{len(baseline_scores)}",
            "new_pass_rate": f"{passing_new}/{len(new_scores)}",
            "baseline_pass_ci": baseline_pass_rate,
            "new_pass_ci": new_pass_rate,
            "status": _determine_status(diff)
        }

        # Track improvements and regressions
        if criterion_report["status"] == "REGRESSION":
            report["regressions"].append(criterion)
        elif criterion_report["status"] == "IMPROVED":
            report["improvements"].append(criterion)

        report["criteria"][criterion] = criterion_report

    # 5. Compute overall statistics across all criteria
    all_baseline = [s.score for r in baseline_results for s in r.scores]
    all_new = [s.score for r in new_results for s in r.scores]

    if all_baseline and all_new:
        report["overall"] = {
            "baseline_mean": round(statistics.mean(all_baseline), 3),
            "new_mean": round(statistics.mean(all_new), 3),
            "diff": round(statistics.mean(all_new) - statistics.mean(all_baseline), 3),
            "n_test_cases": len(baseline_results),
            "ship_decision": "SHIP" if not report["regressions"] else "BLOCK",
        }

    return report


def print_comparison_report(report):
    """
    Formats and prints the comparison report to standard output.
    
    Args:
        report (dict): Comparison report generated by compare_eval_runs.
    """
    print("=" * 70)
    print("  EVAL COMPARISON REPORT")
    print("=" * 70)

    overall = report.get("overall", {})
    decision = overall.get("ship_decision", "UNKNOWN")
    print(f"\n  Decision: {decision}")
    print(f"  Test cases: {overall.get('n_test_cases', 0)}")
    print(f"  Overall: {overall.get('baseline_mean', 0):.3f} -> {overall.get('new_mean', 0):.3f} (diff: {overall.get('diff', 0):+.3f})")

    print(f"\n  {'Criterion':<15} {'Baseline':>10} {'New':>10} {'Diff':>8} {'Status':>12}")
    print(f"  {'-'*55}")
    for criterion, data in report.get("criteria", {}).items():
        print(f"  {criterion:<15} {data['baseline_mean']:>10.3f} {data['new_mean']:>10.3f} {data['diff']:>+8.3f} {data['status']:>12}")
        print(f"  {'':15} CI: {data['baseline_ci']} -> {data['new_ci']}")

    if report.get("regressions"):
        print(f"\n  REGRESSIONS DETECTED: {', '.join(report['regressions'])}")
    if report.get("improvements"):
        print(f"  IMPROVEMENTS: {', '.join(report['improvements'])}")

    print("=" * 70)


def _extract_scores_by_criterion(results, criterion):
    """Helper to extract judge scores for a specific criterion from run results."""
    scores = []
    for r in results:
        for s in r.scores:
            if s.criterion == criterion:
                scores.append(s.score)
    return scores


def _determine_status(diff, threshold=0.3):
    """Helper to classify a score difference as regression, improvement, or stable."""
    if diff < -threshold:
        return "REGRESSION"
    elif diff > threshold:
        return "IMPROVED"
    return "STABLE"
